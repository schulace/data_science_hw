X, y = select(df, Not(:Class)), select(df, :Class)

# ╔═╡ e53f34f3-0419-4bfe-b020-784489f3a0b5
begin
	μ = combine(X, All() .=> mean .=> All())
	σ = combine(X, All() .=> std .=> All())
	X_norm = (X .- μ) ./ σ
	μ, σ, X_norm
end

# ╔═╡ 58f4346e-db96-49ac-991c-e24b92e1968f
# no values are missing here

# ╔═╡ 68443bf9-e9fe-43ed-aaed-03d320ca19bd
(X_train, X_test), (y_train, y_test) = MLJ.partition((X_norm, y), 0.8, shuffle=true, multi=true)

# ╔═╡ f9d41035-bed9-46d7-b500-0d7a7db4267c
@bind n_clusters Slider(2:1:40, show_value=true, default=29)

# ╔═╡ 3f5b1d25-f26f-4e90-8f7a-ae6c478dc4c8
begin
	import BetaML
	GMM = @load GaussianMixtureClusterer pkg="BetaML"
end

# ╔═╡ d3c482bc-0c74-4d89-9f1a-5c9ab56f3ebc
mach = let
	model = GMM(
		n_classes=n_clusters,
		#mixtures=BetaML.GMM.SphericalGaussian,
		mixtures=BetaML.GMM.DiagonalGaussian,
		#mixtures=BetaML.GMM.FullGaussian,
		initialisation_strategy="grid"
	)
	mach = machine(model, X_train)
	fit!(mach)
	mach
end

# ╔═╡ d7a06e68-ede0-44ff-97b9-15ba9ecd5859
test_preds = predict_mode(mach, X_test)

# ╔═╡ 61486bb1-7202-4b62-9664-f944391c14ae
